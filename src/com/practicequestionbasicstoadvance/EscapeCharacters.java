public class EscapeCharacters {
	
	
	public static void main(String[] args) {
		
		System.out.println("Hello"+"\n"); // \n is for starting with new line ::
		
		System.out.println("hii");
		
		
		System.out.println("\n");
		
		System.out.println("HII"+"\t"+"hello"); // \t gives horizontal space while writing in a line 
		
		
             System.out.println("Helllo "+"\b");

		System.out.println("Hello "+"\r");
		
		System.out.println("Gehdahv"+"\f");
	}

}
