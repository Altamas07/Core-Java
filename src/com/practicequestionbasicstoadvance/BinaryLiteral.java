public class BinaryLiteral {
  public static void main(String[] args) {
	   
	  int x=0b1111;  /// this is binary literal this came in 1.7 version of java 
	  
	  System.out.println(x);
	  
	  
	  int v=887654345; // here it is not easy to count how much is it 
	  
	  int b=88_76_54_345;  // here it is easy to understand what number is written it is also 
	                      //came in 1.7 version before 1.7 version "_" symbol is used in only between identifiers ::   
	  
	  System.out.println(v+"..."+b);
	  
	  // after printing both value on console they give same output:: 
	  
	  // it can be used in numeric literals  ::
	  
	  // but also in floating point literals but in some cases ::
	  
	  // we can not use "_" at first place and not before the decimal point and not
	  //in the end after the last digit
	  
} 
  
}
