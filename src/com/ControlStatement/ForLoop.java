package com.controlStatement;

public class ForLoop {
            
	public static void main(String[] args) {
		
		int i=0;
		
		for(System.out.println("hello");i<3;i++) { // inside the for loop u can take any valid java
			                                      // statement at the place of initialization 
			
			
			System.out.println("hii");
		}
		
		int j=0;
		for(;j++<3;) { // you can take increment and condition inside for and initialization 
			          // outside for this is also valid
			
			
			System.out.println("hello");
		}
	}
}
