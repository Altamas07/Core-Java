package com.practicequestionbasicstoadvance;

public class IntegralLiteralWays {
     public static void main(String[] args) {
		
    	 int x=10; // Decimal literal;
    	 
    	 System.out.println(x);
    	 
    	 
    	 int y=0111;// octal decimal ;
    	 
    	 System.out.println(y);
    	 
    	 
    	 int z=0x10; // Hexa decimal; here java is not case sensitive 
    	 
    	 System.out.println(z);
    	 
    	 int h=0b10; /// binary literal ;
    	 
    	 
    	 System.out.println(h);
    	 
    	 int m=23_23;
    	 
    	 System.out.println(m);
    	 
    	 
    	 int n=0xBeef; // hexa decimal 
    	 
    	 System.out.println(n);
    	 
	}
}
