package com.array;

public class EvenOddFromArray {
      public static void main(String[] args) {
		
    	  int[] arr= {54,34,5,65,65,34,232,56};
    	  
    	  
    	  System.out.println("Even numbers are::");
    	  for(int num:arr) {
    		  
    		  if(num%2==0) {
    			  
    			  
    			  
    			  System.out.println(num);
    			  
    		  }
 
    		  }
    	  System.out.println("Odd numbers are::");
    	  for(int num:arr) {
    		  
    		  if(num%2!=0) {
    			  
    			
    			  
    			  System.out.println(+num);
    		  }
    	  }
    	  }
    	  
	}

