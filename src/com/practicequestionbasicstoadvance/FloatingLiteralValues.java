package com.practicequestionbasicstoadvance;

public class FloatingLiteralValues {

	public static void main(String[] args) {
		
		double x=234.56; // decimal literal 
		
		double y=0453.34; // octal literal does not support it only gives value originally in decimal 
		
		
	//	double b=0x234.554; // hexa decimal is not supported in floating point literals
		
		System.out.println(x+" "+y+"");
		
		
		// octal and hexa are not valid in floating point literals
	}
}
